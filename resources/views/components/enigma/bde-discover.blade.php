<p class="description">
    Votre BDE souhaite se présenter. Vous devrez allez les voir chacun à leur tour. Qui sont-ils ?
    <br>
    Pour chaque étape, vous devez entrer le nom de la personne à deviner, et aller la voir pour obtenir l'énigme suivante.
    <br>
    La solution de la dernière étape n'est pas le nom de la dernière personne à deviner. Elle vous donnera le code à
    entrer pour valider l'énigme.
</p>
<br>

<div class="alert green">
    De manière générale, durant les énigmes, la réponse permettant de valider la dernière étape d'une énigme (ou une partie de celle réponse)
    peut être utilisée pour accéder à une autre énigme. (mais pas obligatoirement)
</div>
